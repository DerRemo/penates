// Vendors a curated subset of Catppuccin VSCode file icons (css-variables flavor)
// into public/vendor/catppuccin-icons/. Kein npm-Paket → wir holen die SVGs vom
// GitHub-Raw an einem gepinnten Ref. Die css-variables-Variante färbt via
// var(--vscode-ctp-<color>) → eine Datei retintet sich pro Hub-Flavor.
// Lizenz MIT (catppuccin/vscode-icons).
//
// Zusätzlich wird ein committetes Daten-Modul public/catppuccin-icons-data.js
// generiert (ICON_SVG = { '<key>': '<svg…>…</svg>' }). Grund: ein über <img src>
// geladenes SVG rendert in isoliertem Kontext und kann var(--vscode-ctp-*) der
// Host-Seite NICHT auflösen → die Icons bleiben unsichtbar. Inline-<svg> im DOM
// löst die CSS-Variablen auf und retintet pro Flavor. file-icons.js importiert
// ICON_SVG synchron (wie clis.js seine Logos).
//
// Aufruf:
//   node scripts/vendor-catppuccin-icons.mjs            → fetcht + schreibt SVGs + Daten-Modul
//   node scripts/vendor-catppuccin-icons.mjs --local    → nur Daten-Modul aus vorhandenen SVGs (kein Netz)
import { mkdirSync, rmSync, writeFileSync, readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(ROOT, 'public', 'vendor', 'catppuccin-icons');
const DATA_MODULE = join(ROOT, 'public', 'catppuccin-icons-data.js');

// Gepinnter Ref — bewusst ein konkreter Branch/Tag, damit der Vendor
// reproduzierbar ist. Bei Update: REF + ggf. ICONS anpassen, neu vendoren.
const REF = 'main';
const BASE = `https://raw.githubusercontent.com/catppuccin/vscode-icons/${REF}/icons/css-variables`;

// Lokaler Icon-Key (Dateiname unter vendor/, von file-icons.js referenziert)
// → Quell-SVG-Name im Repo. Wo der Repo-Name vom Key abweicht (react→
// javascript-react, shell→bash, document→text, nodejs→npm) mappen wir explizit,
// damit file-icons.js stabile, sprechende Keys behält. _file/_folder/
// _folder_open sind die Pflicht-Fallbacks. Erweitern = hier ergänzen + neu vendoren.
const ICONS = {
  '_file': '_file', '_folder': '_folder', '_folder_open': '_folder_open',
  'javascript': 'javascript', 'typescript': 'typescript',
  'react': 'javascript-react', 'react_ts': 'typescript-react',
  'json': 'json', 'markdown': 'markdown', 'html': 'html', 'css': 'css', 'sass': 'sass',
  'python': 'python', 'rust': 'rust', 'go': 'go', 'shell': 'bash', 'yaml': 'yaml', 'toml': 'toml',
  'lock': 'lock', 'image': 'image', 'pdf': 'pdf', 'svg': 'svg', 'document': 'text',
  'git': 'git', 'docker': 'docker', 'nodejs': 'npm', 'npm': 'npm', 'vue': 'vue',
  'database': 'database', 'font': 'font',
};

// Liest jede vendored <key>.svg von der Platte und schreibt das ESM-Daten-Modul.
// Reines lokales I/O — kein Netz. Trim auf eine Zeile pro Eintrag (kompakt).
function writeDataModule(keys) {
  const entries = keys.map((key) => {
    const svg = readFileSync(join(OUT, `${key}.svg`), 'utf8').trim();
    if (!svg.startsWith('<svg')) {
      console.error(`vendor-catppuccin-icons: ${key}.svg looks invalid (no <svg> root)`);
      process.exit(1);
    }
    // Vertrauenswürdige vendored Assets (kein <script>/<style>/Event-Handler).
    // Whitespace zwischen Tags zusammenziehen → kompakte Single-Line-Strings.
    const compact = svg.replace(/>\s+</g, '><').replace(/\s+/g, ' ').trim();
    return `  ${JSON.stringify(key)}: ${JSON.stringify(compact)},`;
  });
  const body = `// AUTO-GENERATED by scripts/vendor-catppuccin-icons.mjs — DO NOT EDIT BY HAND.
// Inline-SVG-Markup pro Icon-Key (Catppuccin css-variables). Inline statt <img src>,
// damit var(--vscode-ctp-*) der Host-Seite auflöst und pro Hub-Flavor retintet.
// Quelle: public/vendor/catppuccin-icons/<key>.svg (MIT, catppuccin/vscode-icons).
export const ICON_SVG = {
${entries.join('\n')}
};
`;
  writeFileSync(DATA_MODULE, body);
  console.log(`vendor-catppuccin-icons: wrote ${keys.length} inline-SVG entries to public/catppuccin-icons-data.js`);
}

const localOnly = process.argv.includes('--local');

if (localOnly) {
  // Nur das Daten-Modul aus den bereits vendored SVGs (per manifest) regenerieren.
  let manifest;
  try {
    manifest = JSON.parse(readFileSync(join(OUT, 'manifest.json'), 'utf8'));
  } catch {
    console.error('vendor-catppuccin-icons --local: manifest.json missing — run a full fetch first');
    process.exit(1);
  }
  writeDataModule(manifest.icons);
} else {
  rmSync(OUT, { recursive: true, force: true });
  mkdirSync(OUT, { recursive: true });

  const fetched = [];
  for (const [key, source] of Object.entries(ICONS)) {
    const url = `${BASE}/${source}.svg`;
    const res = await fetch(url);
    if (!res.ok) {
      console.error(`vendor-catppuccin-icons: ${source}.svg → HTTP ${res.status} (${url})`);
      process.exit(1);
    }
    const svg = await res.text();
    if (!svg.includes('<svg')) {
      console.error(`vendor-catppuccin-icons: ${source}.svg looks invalid`);
      process.exit(1);
    }
    writeFileSync(join(OUT, `${key}.svg`), svg);
    fetched.push(key);
  }

  // LICENSE des Repos mitziehen (MIT).
  const lic = await fetch(`https://raw.githubusercontent.com/catppuccin/vscode-icons/${REF}/LICENSE`);
  if (lic.ok) writeFileSync(join(OUT, 'LICENSE'), await lic.text());

  writeFileSync(
    join(OUT, 'manifest.json'),
    JSON.stringify({ source: 'catppuccin/vscode-icons', flavor: 'css-variables', ref: REF, icons: fetched }, null, 2) + '\n'
  );
  console.log(`vendor-catppuccin-icons: wrote ${fetched.length} icons (ref ${REF}) to public/vendor/catppuccin-icons/`);

  // Daten-Modul aus den frisch geschriebenen SVGs generieren.
  writeDataModule(fetched);
}
